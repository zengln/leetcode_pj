    def nthUglyNumber(self, n: int) -> int:
        result = [1]
        p2 = 0
        p3 = 0
        p5 = 0
        for i in range(1, n):
            temp = 1
            while temp in result:
                temp = min(result[p2] * 2, result[p3] * 3, result[p5] * 5)
                if temp == result[p2] * 2:
                    p2 += 1
                elif temp == result[p3] * 3:
                    p3 += 1
                else:
                    p5 += 1
            result.append(temp)
        return result[-1]

