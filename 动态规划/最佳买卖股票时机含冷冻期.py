    def maxProfit(self, prices: List[int]) -> int:
        dp_y = [0] * len(prices)
        dp_n = [0] * len(prices)
        dp_l = [0] * len(prices)
        dp_y[0] = -prices[0]
        for i in range(1, len(prices)):
            dp_y[i] = max(dp_y[i-1], dp_l[i-1] - prices[i])
            dp_n[i] = max(dp_y[i-1] + prices[i], dp_n[i-1], dp_l[i-1])
            dp_l[i] = dp_n[i-1]

        return dp_l[-1] if dp_l[-1] > dp_n[-1] else dp_n[-1]


