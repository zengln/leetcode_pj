class Solution:
    """
    éå†æ‰€æœ‰çš„ç‚¹ï¼Œå½“å½“å‰ç‚¹ä¸º1æ—¶ï¼Œè®¡ç®—å½“å‰ç‚¹å…³è”åˆ°çš„å²›å±¿çš„é¢ç§¯
    å²›å±¿é¢ç§¯è®¡ç®—èƒ½ä½¿ç”¨å¹¿åº¦ä¼˜å…ˆ
    """
    def maxAreaOfIsland(self, grid: List[List[int]]) -> int:
        result = 0
        r, c = len(grid), len(grid[0])
        for x in range(r):
            for y in range(c):
                if grid[x][y] != 1:
                    continue
                queue = [(x, y)]
                grid[x][y] = 0
                temp = 1
                while queue:
                    x, y = queue.pop(0)
                    for x, y in [(x+1, y), (x-1, y), (x, y+1), (x, y-1)]:
                        if 0 <= x < r and 0 <= y < c and grid[x][y] == 1:
                            temp += 1
                            grid[x][y] = 0
                            queue.append((x, y))
                result = max(result, temp)

        return result
